#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "../..");
const manifestPath = resolve(repoRoot, "schemas/app-server/roder-app-server.v1.json");
const defaultOutput = resolve(repoRoot, "sdk/typescript/src/types.generated.ts");

const args = process.argv.slice(2);
const check = args.includes("--check");
const output = optionValue(args, "--output") ?? defaultOutput;

const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
const text = render(manifest);

if (check) {
  const current = readFileSync(output, "utf8");
  if (current !== text) {
    console.error(`${relative(output)} is stale; run node sdk/codegen/generate-typescript.mjs`);
    process.exit(1);
  }
} else {
  await mkdir(dirname(output), { recursive: true });
  writeFileSync(output, text);
}

function render(manifest) {
  const methods = manifest.methods.map((method) => method.method);
  return `// Generated by sdk/codegen/generate-typescript.mjs. Do not edit.

export const appServerManifest = ${JSON.stringify(manifest, null, 2)} as const;

export type AppServerMethod = typeof appServerManifest.methods[number]["method"];

export const appServerMethods = ${JSON.stringify(methods, null, 2)} as const;

export type JsonRpcId = string | number | null;

export interface JsonRpcRequest<M extends AppServerMethod = AppServerMethod, P = unknown> {
  jsonrpc: "2.0";
  id?: JsonRpcId;
  method: M;
  params?: P;
}

export interface JsonRpcError {
  code: number;
  message: string;
  data?: unknown;
}

export interface JsonRpcResponse<R = unknown> {
  jsonrpc: "2.0";
  id?: JsonRpcId;
  result?: R;
  error?: JsonRpcError;
}
`;
}

function optionValue(args, name) {
  const equals = args.find((arg) => arg.startsWith(`${name}=`));
  if (equals) {
    return equals.slice(name.length + 1);
  }
  const index = args.indexOf(name);
  return index === -1 ? undefined : args[index + 1];
}

function relative(path) {
  return path.startsWith(`${repoRoot}/`) ? path.slice(repoRoot.length + 1) : path;
}
