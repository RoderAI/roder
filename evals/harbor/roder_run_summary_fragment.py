"""Generate the shell fragment that writes a Roder Harbor run summary."""

from __future__ import annotations

import shlex


def run_summary_shell_fragment(
    *,
    provider: str,
    model: str,
    reasoning: str,
    policy_mode: str,
    task_ledger_required: bool,
    soft_timeout_sec: int | None,
    eval_deadline_seconds: int | None,
    config_dir: str,
    events_path: str,
    stderr_path: str,
    output_path: str,
    last_message_path: str,
    run_summary_path: str,
) -> str:
    soft_timeout = "" if soft_timeout_sec is None else str(soft_timeout_sec)
    eval_deadline = "" if eval_deadline_seconds is None else str(eval_deadline_seconds)
    task_ledger = "1" if task_ledger_required else "0"
    return (
        "finished_at=$(date -u '+%Y-%m-%dT%H:%M:%SZ')\n"
        "end_epoch=$(date +%s)\n"
        "elapsed_seconds=$((end_epoch - start_epoch))\n"
        f"RODER_RUN_SUMMARY_PATH={shlex.quote(run_summary_path)} "
        f"RODER_EVENTS_PATH={shlex.quote(events_path)} "
        f"RODER_STDERR_PATH={shlex.quote(stderr_path)} "
        f"RODER_OUTPUT_PATH={shlex.quote(output_path)} "
        f"RODER_LAST_MESSAGE_PATH={shlex.quote(last_message_path)} "
        f"RODER_VERSION_PATH=/logs/agent/roder-version.txt "
        f"RODER_PROVIDER={shlex.quote(provider)} "
        f"RODER_MODEL={shlex.quote(model)} "
        f"RODER_REASONING={shlex.quote(reasoning)} "
        f"RODER_POLICY_MODE={shlex.quote(policy_mode)} "
        f"RODER_TASK_LEDGER_REQUIRED={task_ledger} "
        f"RODER_CONFIG_DIR_VALUE={shlex.quote(config_dir)} "
        f"RODER_SOFT_TIMEOUT_SEC={shlex.quote(soft_timeout)} "
        f"RODER_EVAL_DEADLINE_SECONDS={shlex.quote(eval_deadline)} "
        "RODER_EXIT_STATUS=\"$status\" "
        "RODER_SOFT_TIMED_OUT=\"$soft_timed_out\" "
        "RODER_DEADLINE_TIMED_OUT=\"$deadline_timed_out\" "
        "RODER_STARTED_AT=\"$started_at\" "
        "RODER_FINISHED_AT=\"$finished_at\" "
        "RODER_ELAPSED_SECONDS=\"$elapsed_seconds\" "
        "python3 - <<'PY' || true\n"
        "import json, os\n"
        "from pathlib import Path\n"
        "\n"
        "def env_int(name):\n"
        "    value = os.environ.get(name, '')\n"
        "    if value == '':\n"
        "        return None\n"
        "    try:\n"
        "        return int(value)\n"
        "    except ValueError:\n"
        "        return None\n"
        "\n"
        "def env_bool(name):\n"
        "    return os.environ.get(name) in {'1', 'true', 'True'}\n"
        "\n"
        "def read(path):\n"
        "    try:\n"
        "        return Path(path).read_text(errors='replace')\n"
        "    except OSError:\n"
        "        return ''\n"
        "\n"
        "def size(path):\n"
        "    try:\n"
        "        return Path(path).stat().st_size\n"
        "    except OSError:\n"
        "        return 0\n"
        "\n"
        "def last_event(path):\n"
        "    try:\n"
        "        lines = Path(path).read_text(errors='replace').splitlines()\n"
        "    except OSError:\n"
        "        return None\n"
        "    for line in reversed(lines):\n"
        "        if not line.strip():\n"
        "            continue\n"
        "        try:\n"
        "            event = json.loads(line)\n"
        "        except json.JSONDecodeError:\n"
        "            return {'raw': line[:500]}\n"
        "        item = event.get('item') if isinstance(event, dict) else None\n"
        "        out = {'type': event.get('type')}\n"
        "        if isinstance(item, dict):\n"
        "            out.update({\n"
        "                'item_type': item.get('type'),\n"
        "                'tool_name': item.get('tool_name'),\n"
        "                'status': item.get('status'),\n"
        "                'id': item.get('id'),\n"
        "            })\n"
        "        if isinstance(event, dict) and event.get('error'):\n"
        "            out['error'] = str(event.get('error'))[:500]\n"
        "        return {key: value for key, value in out.items() if value is not None}\n"
        "    return None\n"
        "\n"
        "def compact(value, limit=1000):\n"
        "    if value is None:\n"
        "        return None\n"
        "    if isinstance(value, str):\n"
        "        text = value\n"
        "    else:\n"
        "        try:\n"
        "            text = json.dumps(value, sort_keys=True)\n"
        "        except TypeError:\n"
        "            text = str(value)\n"
        "    text = text.replace('\\x00', '')\n"
        "    return text if len(text) <= limit else text[:limit] + '...'\n"
        "\n"
        "def tool_snapshot(event_type, item):\n"
        "    if not isinstance(item, dict) or not item.get('tool_name'):\n"
        "        return None\n"
        "    payload = item.get('payload') if isinstance(item.get('payload'), dict) else {}\n"
        "    payload_out = {}\n"
        "    for key in ('cmd', 'command', 'query', 'action', 'path', 'name'):\n"
        "        if key in payload:\n"
        "            payload_out[key] = compact(payload.get(key))\n"
        "    out = {\n"
        "        'event_type': event_type,\n"
        "        'id': item.get('id'),\n"
        "        'tool_call_id': item.get('tool_call_id'),\n"
        "        'tool_name': item.get('tool_name'),\n"
        "        'item_type': item.get('type'),\n"
        "        'status': item.get('status'),\n"
        "    }\n"
        "    if payload_out:\n"
        "        out['payload'] = payload_out\n"
        "    if item.get('type') == 'toolMessage' and item.get('text'):\n"
        "        out['text_preview'] = compact(item.get('text'), 1200)\n"
        "    return {key: value for key, value in out.items() if value is not None}\n"
        "\n"
        "def tool_activity(path):\n"
        "    active = {}\n"
        "    last_tool = None\n"
        "    try:\n"
        "        lines = Path(path).read_text(errors='replace').splitlines()\n"
        "    except OSError:\n"
        "        return {'active_tool': None, 'last_tool': None}\n"
        "    for line in lines:\n"
        "        if not line.strip():\n"
        "            continue\n"
        "        try:\n"
        "            event = json.loads(line)\n"
        "        except json.JSONDecodeError:\n"
        "            continue\n"
        "        event_type = event.get('type') if isinstance(event, dict) else None\n"
        "        item = event.get('item') if isinstance(event, dict) else None\n"
        "        snap = tool_snapshot(event_type, item)\n"
        "        if not snap:\n"
        "            continue\n"
        "        key = snap.get('tool_call_id') or snap.get('id')\n"
        "        if event_type == 'item.started' and item.get('status') == 'inProgress':\n"
        "            active[key] = snap\n"
        "        elif event_type == 'item.completed':\n"
        "            last_tool = snap\n"
        "            active.pop(key, None)\n"
        "    active_tool = list(active.values())[-1] if active else None\n"
        "    return {'active_tool': active_tool, 'last_tool': last_tool}\n"
        "\n"
        "def stderr_noise_kind(text):\n"
        "    if text and text.replace('\\a', '').strip() == '':\n"
        "        return 'bel_only'\n"
        "    return None\n"
        "\n"
        "stderr_text = read(os.environ['RODER_STDERR_PATH'])\n"
        "output_text = read(os.environ['RODER_OUTPUT_PATH'])\n"
        "combined = stderr_text + '\\n' + output_text\n"
        "activity = tool_activity(os.environ['RODER_EVENTS_PATH'])\n"
        "provider_error_kind = None\n"
        "if 'flagged for possible cybersecurity risk' in combined:\n"
        "    provider_error_kind = 'policy_block'\n"
        "elif 'refresh_token_reused' in combined:\n"
        "    provider_error_kind = 'auth_refresh_token_reused'\n"
        "elif 'turn deadline expired' in combined:\n"
        "    provider_error_kind = 'turn_deadline_expired'\n"
        "elif \"Invalid 'input[\" in combined and 'string does not match pattern' in combined:\n"
        "    provider_error_kind = 'invalid_tool_name'\n"
        "elif 'error decoding response body' in combined:\n"
        "    provider_error_kind = 'stream_decode_error'\n"
        "elif 'stream closed before response.completed' in combined:\n"
        "    provider_error_kind = 'stream_incomplete'\n"
        "deadline_finalized = (\n"
        "    'deadline finalization' in combined.lower()\n"
        "    or 'before the deadline' in combined.lower()\n"
        ")\n"
        "\n"
        "summary = {\n"
        "    'schema_version': 1,\n"
        "    'provider': os.environ.get('RODER_PROVIDER'),\n"
        "    'model': os.environ.get('RODER_MODEL'),\n"
        "    'reasoning': os.environ.get('RODER_REASONING'),\n"
        "    'policy_mode': os.environ.get('RODER_POLICY_MODE'),\n"
        "    'task_ledger_required': env_bool('RODER_TASK_LEDGER_REQUIRED'),\n"
        "    'config_dir': os.environ.get('RODER_CONFIG_DIR_VALUE'),\n"
        "    'soft_timeout_sec': env_int('RODER_SOFT_TIMEOUT_SEC'),\n"
        "    'eval_deadline_seconds': env_int('RODER_EVAL_DEADLINE_SECONDS'),\n"
        "    'started_at': os.environ.get('RODER_STARTED_AT'),\n"
        "    'finished_at': os.environ.get('RODER_FINISHED_AT'),\n"
        "    'elapsed_seconds': env_int('RODER_ELAPSED_SECONDS'),\n"
        "    'exit_status': env_int('RODER_EXIT_STATUS'),\n"
        "    'soft_timed_out': env_bool('RODER_SOFT_TIMED_OUT'),\n"
        "    'deadline_timed_out': env_bool('RODER_DEADLINE_TIMED_OUT'),\n"
        "    'deadline_finalized': deadline_finalized,\n"
        "    'provider_error_kind': provider_error_kind,\n"
        "    'stderr_noise_kind': stderr_noise_kind(stderr_text),\n"
        "    'artifacts': {\n"
        "        'events_bytes': size(os.environ['RODER_EVENTS_PATH']),\n"
        "        'stderr_bytes': size(os.environ['RODER_STDERR_PATH']),\n"
        "        'output_bytes': size(os.environ['RODER_OUTPUT_PATH']),\n"
        "        'last_message_bytes': size(os.environ['RODER_LAST_MESSAGE_PATH']),\n"
        "    },\n"
        "    'last_event': last_event(os.environ['RODER_EVENTS_PATH']),\n"
        "    'active_tool': activity['active_tool'],\n"
        "    'last_tool': activity['last_tool'],\n"
        "    'roder_version': read(os.environ['RODER_VERSION_PATH']).strip() or None,\n"
        "}\n"
        "Path(os.environ['RODER_RUN_SUMMARY_PATH']).write_text(json.dumps(summary, indent=2) + '\\n')\n"
        "PY\n"
    )
